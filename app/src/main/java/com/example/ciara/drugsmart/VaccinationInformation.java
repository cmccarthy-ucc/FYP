package com.example.ciara.drugsmart;

public class VaccinationInformation {
    private String vaccinationAdmin;
    private String vaccinationDate;
    private String vaccinationAnimalID;
    private String animalTag;
    private String drugType;
    private String dosage;
    private String vaccinationID;

    public VaccinationInformation(){

    }

    public String getVaccinationAdmin() {
        return vaccinationAdmin;
    }

    public void setVaccinationAdmin(String vaccinationAdmin) {
        this.vaccinationAdmin = vaccinationAdmin;
    }

    public String getVaccinationDate() {
        return vaccinationDate;
    }

    public void setVaccinationDate(String vaccinationDate) {
        this.vaccinationDate = vaccinationDate;
    }

    public String getVaccinationAnimalID() {
        return vaccinationAnimalID;
    }

    public void setVaccinationAnimalID(String vaccinationAnimalID) {
        this.vaccinationAnimalID = vaccinationAnimalID;
    }

    public String getAnimalTag() {
        return animalTag;
    }

    public void setAnimalTag(String animalTag) {
        this.animalTag = animalTag;
    }

    public String getDrugType() {
        return drugType;
    }

    public void setDrugType(String drugType) {
        this.drugType = drugType;
    }

    public String getDosage() {
        return dosage;
    }

    public void setDosage(String dosage) {
        this.dosage = dosage;
    }

    public String getVaccinationID() {
        return vaccinationID;
    }

    public void setVaccinationID(String vaccinationID) {
        this.vaccinationID = vaccinationID;
    }




}
